package br.com.gradeacademic.entidade;

public class Horario {

	private int id;
	private String horario_professor;
	private String horario_curso;
	private String horario_semestre;
	private String horario_dia;
	private String horario_disciplina;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getHorario_professor() {
		return horario_professor;
	}

	public void setHorario_professor(String horario_professor) {
		this.horario_professor = horario_professor;
	}

	public String getHorario_curso() {
		return horario_curso;
	}

	public void setHorario_curso(String horario_curso) {
		this.horario_curso = horario_curso;
	}

	public String getHorario_semestre() {
		return horario_semestre;
	}

	public void setHorario_semestre(String horario_semestre) {
		this.horario_semestre = horario_semestre;
	}

	public String getHorario_dia() {
		return horario_dia;
	}

	public void setHorario_dia(String horario_dia) {
		this.horario_dia = horario_dia;
	}

	public String getHorario_disciplina() {
		return horario_disciplina;
	}

	public void setHorario_disciplina(String horario_disciplina) {
		this.horario_disciplina = horario_disciplina;
	}

}
